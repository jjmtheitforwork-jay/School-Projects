    private void displaytabledata()
    {
        
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb","root", "root");
            String q = "select * from "+cboTable.getSelectedItem()+"";
            PreparedStatement pstmt = con.prepareStatement(q);
            ResultSet rs = pstmt.executeQuery();
            tbldata.setModel(DbUtils.resultSetToTableModel(rs));
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }
    private void tableInCombo()
    {
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb", "root","root");
            DatabaseMetaData dbmd = con.getMetaData();
            String[] type = {"TABLE"};
            ResultSet rs = dbmd.getTables("tvrepairdb", null,"%", type);
            
            while(rs.next())
            {
                cboTable.addItem(rs.getString("TABLE_NAME"));
            }
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

