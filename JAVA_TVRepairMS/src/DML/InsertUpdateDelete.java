    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb","root","root");
            Statement stmt = con.createStatement();
            String tableName = cboTable.getSelectedItem().toString();
            DefaultTableModel m = (DefaultTableModel) tbldata.getModel();
            int rowIndex = tbldata.getSelectedRow();
            if(rowIndex == -1)
            {
                JOptionPane.showMessageDialog(this, "Please select a row to update.");
                return;
            }
            if (tbldata.isEditing())
            {
                tbldata.getCellEditor().stopCellEditing();
            }
            String q = "update " + tableName + " set ";
            for (int i = 1; i < m.getColumnCount(); i++)
            {
                Object value = m.getValueAt(rowIndex, i);
                String colName = m.getColumnName(i);
                q += colName + "='" + value + "'";
                if (i < m.getColumnCount() - 1)
                {
                    q += ",";
                }
            }
            Object pkValue = m.getValueAt(rowIndex, 0);
            String pkColumn = m.getColumnName(0);
            q += " where " + pkColumn + "='" + pkValue + "'";
            stmt.execute(q);
            JOptionPane.showMessageDialog(this, "Data updated successfully in '" + tableName + "'.");
            displaytabledata();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
        
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb","root","root");
            Statement stmt = con.createStatement();
            String tableName = cboTable.getSelectedItem().toString();
            DefaultTableModel m = (DefaultTableModel) tbldata.getModel();
            int rowIndex = tbldata.getSelectedRow();
            
            if(rowIndex == -1)
            {
                JOptionPane.showMessageDialog(this, "Please select a row to delete.");
                return;
            }
            
            String pkColumn = m.getColumnName(0);
            Object pkValue = m.getValueAt(rowIndex, 0);
            int choice = JOptionPane.showConfirmDialog(this, 
                        "Are you sure you want to delete this row : '" + pkColumn + " = " + pkValue + "' ?",
                        "Confirm Delete", JOptionPane.YES_NO_OPTION);
            if(choice == JOptionPane.YES_OPTION)
            {
                String q = "DELETE FROM " + tableName + " WHERE " + pkColumn + "='" + pkValue + "'";
                stmt.execute(q);
                JOptionPane.showMessageDialog(this, "Data deleted successfully from '" + tableName + "'.");
                displaytabledata();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Delete cancelled.");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
        displaytabledata();
    }//GEN-LAST:event_btndeleteActionPerformed

