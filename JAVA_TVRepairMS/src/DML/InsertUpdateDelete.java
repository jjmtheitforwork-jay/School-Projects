    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb","root","root");
            Statement stmt = con.createStatement();
            String tableName = cboTable.getSelectedItem().toString();
            DefaultTableModel m = (DefaultTableModel) tbldata.getModel();
            int rowIndex = tbldata.getSelectedRow(); //Get the index of the row the user selected
            if(rowIndex == -1)  //If no row is selected (index = -1)
            {
                JOptionPane.showMessageDialog(this, "Please select a row to insert.");
                return;
            }
            if (tbldata.isEditing())
            {
                tbldata.getCellEditor().stopCellEditing(); //to save latest typed values into the model
            }
            String q = "insert into " + tableName + " values (";    //insert query with for loop 
            for (int i = 0; i < m.getColumnCount(); i++)
            {
                Object value = m.getValueAt(rowIndex, i);
                q += "'" + value + "'";
                if (i < m.getColumnCount() - 1) 
                {
                    q += ",";
                }
            }
            q += ")";
            stmt.execute(q);
            JOptionPane.showMessageDialog(this, "New data inserted successfully into '" + tableName+"'.");
            displaytabledata(); 
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error inserting row: " + ex.getMessage());
        }
    }//GEN-LAST:event_btninsertActionPerformed

