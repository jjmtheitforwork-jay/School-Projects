    private void btndropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndropActionPerformed
        // TODO add your handling code here:
        if(cboTable.getSelectedItem().equals("Select"))
        {
            JOptionPane.showMessageDialog(this, "Please select a table name!");
        }
        else
        {
            int choice = JOptionPane.showConfirmDialog(this, 
               "Are you sure you want to delete this table: " +cboTable.getSelectedItem()+"?",
               "Confirm Delete",JOptionPane.YES_NO_OPTION);
            
            if(choice == JOptionPane.YES_OPTION)
            {
                try
                {
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb", "root","root");
                    String sql = "drop table "+cboTable.getSelectedItem()+"";
                    PreparedStatement pstmt = con.prepareStatement(sql);
                    pstmt.execute();
                    JOptionPane.showMessageDialog(this, cboTable.getSelectedItem()+"Table is deleted.");
                    cboTable.removeAllItems();
                    cboTable.addItem("Select");
                    tableInCombo();

                }catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(this, ex);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Delete cancelled.");
            }
        }
        
        
    }//GEN-LAST:event_btndropActionPerformed

