    private void displaydata()
    {
        try 
        {
            // TODO add your handling code here:
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb","root", "root");
            String q = "describe "+cboTable.getSelectedItem()+"";
            PreparedStatement pstmt = con.prepareStatement(q);
            ResultSet rs = pstmt.executeQuery();
            tbldata.setModel(DbUtils.resultSetToTableModel(rs));
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(this, ex);
        }
    }
    
    private void tableInCombo()
    {
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tvrepairdb", "root","root");
            DatabaseMetaData dbmd = con.getMetaData();
            String[] type = {"TABLE"};
            ResultSet rs = dbmd.getTables("tvrepairdb", null,"%", type);
            cboTable.removeAllItems();
            cboTable.addItem("Select");
            while(rs.next())
            {
                String tableName = rs.getString("TABLE_NAME");
                if(!tableName.equalsIgnoreCase("customer") &&
               !tableName.equalsIgnoreCase("tvproductsandservices") &&
               !tableName.equalsIgnoreCase("userlogin"))    // not to show the built-in tables in the combo selection
                {
                    cboTable.addItem(tableName);
                }  
            }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

